"""USAGE OF LIST IN PYTHON
PROGRAM FOR SHOWING USE OF LIST IN PYTHON
"""

print("USE OF LIST IN PYTHON")

l1 = ["bca","bba","bcom","mca","mba","mcom"]
print(l1)

print("ELEMENT OF LIST AT POSITION =0")
print(l1[0])

print("ELEMENT OF LIST AT POSITION =4")
print(l1[4])

print("PRINT ALL ELEMENTS IN LIST")
print(l1[:6])

print("PRINT LIST ITEMS USING NEGATIVE INDEXING")
print(l1[-2])

print(l1[2:4])

print(l1[:3])

print(l1[4:])

print(l1[-4:-3])

print("Check if Item Exists")
if "bcom" in l1:
    print("YES FOUND IN LIST1")
else:
    print("NO NOT FOUND IN LIST1")




    
